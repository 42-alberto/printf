#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*s_trimmed;
	size_t	start;
	size_t	end;

	if (!s1 && !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	s_trimmed = ft_substr(s1, start, (end - start));
	return (s_trimmed);
}
